#include "3-calc.h"

/**
 * op_add - Additionne deux entiers
 * @a: premier entier
 * @b: second entier
 *
 * Return: la somme de a et b
 */
int op_add(int a, int b)
{
    return (a + b);
}


int op_sub(int a, int b)
{
	return (a - b);
}

int op_mul(int a, int b)
{
	return (a * b);
}

int op_div(int a, int b)
{
	return (a / b);
}

int op_mod(int a, int b)
{
	return (a % b);
}
